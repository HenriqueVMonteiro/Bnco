    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String SenhaDigitada = jPasswordField2.getText();

        try {
            SenhaDigitada = Encrypt.encrypt(SenhaDigitada);
        } catch (Exception a) {
            a.printStackTrace();
        }

        if (jPasswordField1.getText().equals(Login_Cliente.Usuario_Logado.getPassword())) {
            Login_Cliente.Usuario_Logado.setPassword(jPasswordField2.getText());
            String aux = "";
            try {
                BufferedReader br = new BufferedReader(new FileReader("login_cliente.txt"));
                String lineReading = br.readLine();

                while (lineReading != null) {
                    aux = aux.concat(lineReading + "\n");
                    lineReading = br.readLine();
                }

                br.close();

                String[] linhas = aux.split("\n");

                for (int i = 0; i < linhas.length; i++) {
                    String linha = linhas[i];

                    if (i + 1 < linhas.length && linha.equals(Login_Cliente.Usuario_Logado.getUsername())) {
                        linhas[i + 1] = SenhaDigitada;

                        System.out.println("Senha nova :" + Login_Cliente.Usuario_Logado.getPassword());
                    }
                }

                BufferedWriter bw = new BufferedWriter(new FileWriter("login_cliente.txt"));

                bw.write(String.join("\n", linhas));
                bw.newLine();
                bw.close();
                JOptionPane.showMessageDialog(null, "Senha alterada com sucesso!");

            } catch (IOException e) {
                e.printStackTrace();
            }

        }
        System.out.println("Senha nova :" + Login_Cliente.Usuario_Logado.getPassword());

    }//GEN-LAST:event_jButton1ActionPerformed

